    private void btnListadoSala1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListadoSala1ActionPerformed
        String nombreSala = cboSalas.getSelectedItem().toString();
        int idSala = 0;
        for (Salas x : listaSalas) {
            if (x.getDescripcion().equalsIgnoreCase(nombreSala)) {
                idSala = x.getIdSala();
            }
        }
        try {
            Connection conexion = DriverManager.getConnection(CADENA, USER, PASS);
            JasperReport archivo;
            archivo = JasperCompileManager.compileReport("reports/ListaSala.jrxml");
            Map<String, Object> map = new HashMap<String, Object>();
            map.put("pIdSala", idSala);
            map.put("IconReport", "Imagenes/icons8-business-report-64.png");
            JasperPrint prin = JasperFillManager.fillReport(archivo, map, conexion);
            if (prin.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(this, "La sala no contiene alumnos");
            } else {
                JOptionPane.showMessageDialog(null, "Reporte generado");

                JasperViewer ver = new JasperViewer(prin, false);

                ver.setVisible(true);
                ver.setLocationRelativeTo(null);
            }
        } catch (JRException | SQLException ex) {
        }
    }//GEN-LAST:event_btnListadoSala1ActionPerformed

