    private void btnListadoFecNacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListadoFecNacActionPerformed
      try {
            Connection conexion = DriverManager.getConnection(CADENA, USER, PASS);
            int año = Integer.parseInt(txtReporteEstu.getText());
            JasperReport archivo;
            archivo = JasperCompileManager.compileReport("reports/ListaEstudiante.jrxml");
            Map<String, Object> map = new HashMap<String, Object>();
            map.put("año", año);
            map.put("IconReport", "Imagenes/icons8-business-report-64.png");
            JasperPrint prin = JasperFillManager.fillReport(archivo, map, conexion);
            if (prin.getPages().isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay alumnos que nacieron en este año");
            } else {
                JOptionPane.showMessageDialog(null, "Reporte generado");
                JasperViewer ver = new JasperViewer(prin, false);
                ver.setVisible(true);
                ver.setLocationRelativeTo(null);
            }
        } catch (JRException ex) {
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Reportes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnListadoFecNacActionPerformed

