    private void btnInsertAulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertAulaActionPerformed
        try {
            Connection conexion = DriverManager.getConnection(CADENA, USER, PASS);
            PreparedStatement ps = conexion.prepareStatement("insert into Salas values(?,?,?,?)");
            if (txtNombreSala.getText().trim() == null || txtTurno.getText().trim().isEmpty() || txtHoraEntrada.getText().trim().equals("") || txtHoraSalida.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Falta ingresar datos, revise por favor");
            } else {
                ps.setString(1, txtNombreSala.getText());
                ps.setString(2, txtTurno.getText());
                ps.setString(3, txtHoraEntrada.getText());
                ps.setString(4, txtHoraSalida.getText());

                ps.execute();

                ps.close();
                actualizarTabla();
                limpiar();
                Desactivar();
                JOptionPane.showMessageDialog(null, "Cargado con exito");
            }
            conexion.close();
        } catch (Exception ex) {
            System.out.println("Error metodo AGREGAR SALA " + ex);
        }
    }//GEN-LAST:event_btnInsertAulaActionPerformed

