    public void actualizarTabla() {
        DefaultTableModel tmodel = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        ArrayList<Salas> list = g.getSalas();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);
        tmodel.setColumnIdentifiers(new Object[]{"NroSala", "Nombre", "Turno", "Hora de Entrada", "Hora de Salida"});
        for (Salas salas : list) {
            tmodel.addRow(new Object[]{salas.getIdSala(), salas.getDescripcion(), salas.getTurno(), salas.getHoraEntrada(), salas.getHoraSalida()});
        }
        tblRegAula.setModel(tmodel);
        tblRegAula.getTableHeader().setReorderingAllowed(false);
        tblRegAula.getTableHeader().setBackground(new Color(153, 255, 153));
        tblRegAula.getTableHeader().setFont(new Font("Century Gothic", Font.BOLD, 12));
        tblRegAula.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tblRegAula.getColumnModel().getColumn(0).setMaxWidth(0);
        tblRegAula.getColumnModel().getColumn(0).setMinWidth(0);
        tblRegAula.getColumnModel().getColumn(0).setPreferredWidth(0);
        tblRegAula.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        tblRegAula.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
        tblRegAula.getColumnModel().getColumn(3).setCellRenderer(centerRenderer);
        tblRegAula.getColumnModel().getColumn(4).setCellRenderer(centerRenderer);
    }

