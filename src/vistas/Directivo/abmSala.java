    private void btnCronogramaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCronogramaActionPerformed
        idSalaModificar = (int) tblRegAula.getValueAt(tblRegAula.getSelectedRow(), 0);
        ArrayList<Salas> listaS = g.getSalas();
        for (Salas salas : listaS) {
            if (salas.getIdSala() == idSalaModificar) {
                if ("Mañana".equalsIgnoreCase(salas.getTurno())) {
                    ListaHorariosMañana lhm = new ListaHorariosMañana();
                    lhm.setVisible(true);
                    lhm.actualizarPanel(idSalaModificar);
                } else {
                    ListaHorariosTarde lht = new ListaHorariosTarde();
                    lht.setVisible(true);
                    lht.actualizarPanel(idSalaModificar);
                }
            }
        }
    }//GEN-LAST:event_btnCronogramaActionPerformed

