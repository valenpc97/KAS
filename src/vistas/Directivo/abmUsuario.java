    private void btnGuardarModUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarModUserActionPerformed
        String usuario = txtUser.getText();
        String clave = txtClave.getText();
        int idTipoLogin = 0;
        for (TiposLogins tiposLogins : listLog) {
            if (tiposLogins.getDescripcion().equals(cboTipoLogin.getSelectedItem())) {
                idTipoLogin = (tiposLogins.getIdTipoLogin());
            }
        }

        Usuarios u = new Usuarios(usuario, clave, idTipoLogin, idUserModificar);

        g.ModificarUsuario(u);
        actualizarTabla();
        limpiar();
        btnUpdateUser.setEnabled(false);
        btnInsertUser.setEnabled(false);
        btnDeleteUser.setEnabled(false);
        btnNuevoUser.setEnabled(true);
        btnGuardarModUser.setEnabled(false);
        JOptionPane.showMessageDialog(null, "Guardado con exito");
    }//GEN-LAST:event_btnGuardarModUserActionPerformed

    private void btnInsertUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertUserActionPerformed
        try {
            Connection conexion = DriverManager.getConnection(CADENA, USER, PASS);
            PreparedStatement ps = conexion.prepareStatement("insert into Usuarios values (?,?,?)");
            if (txtUser.getText().trim().equals("") || txtClave.getText().trim().equals("") || cboTipoLogin.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(this, "Faltan datos de cargar");
            }else{
            ps.setString(1, txtUser.getText());
            ps.setString(2, txtClave.getText());
            for (TiposLogins tiposLogins : listLog) {
                if (tiposLogins.getDescripcion().equals(cboTipoLogin.getSelectedItem())) {
                    ps.setInt(3, tiposLogins.getIdTipoLogin());
                }
            }
            ps.execute();

            ps.close();
            limpiar();
            Desactivar();
            JOptionPane.showMessageDialog(null, "Cargado con exito");
            }
            conexion.close();
            actualizarTabla();
        } catch (Exception ex) {
            System.out.println("Error metodo AGREGAR USUARIO " + ex);
        }
    }//GEN-LAST:event_btnInsertUserActionPerformed

