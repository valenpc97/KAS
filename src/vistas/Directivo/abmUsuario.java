    public void actualizarTabla() {
        DefaultTableModel tmodel = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        ArrayList<Usuarios> list = g.getUsuarios();
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        tmodel.setColumnIdentifiers(new Object[]{"Id Usuario", "Usuarios", "Tipo de Perfil",});
        for (Usuarios usuarios : list) {
            tmodel.addRow(new Object[]{usuarios.getIdLogin(), usuarios.getUsuario(), usuarios.getDescripcion()});
            tmodel.isCellEditable(1, 1);
        }
        tblRegUser.setModel(tmodel);
        tblRegUser.getTableHeader().setReorderingAllowed(false);
        tblRegUser.getTableHeader().setBackground(new Color(153, 255, 153));
        tblRegUser.getTableHeader().setFont(new Font("Century Gothic", Font.BOLD, 12));
        tblRegUser.getColumnModel().getColumn(0).setCellRenderer(centerRenderer);
        tblRegUser.getColumnModel().getColumn(0).setMaxWidth(0);
        tblRegUser.getColumnModel().getColumn(0).setMinWidth(0);
        tblRegUser.getColumnModel().getColumn(0).setPreferredWidth(0);
        tblRegUser.getColumnModel().getColumn(1).setCellRenderer(centerRenderer);
        tblRegUser.getColumnModel().getColumn(2).setCellRenderer(centerRenderer);
    }

