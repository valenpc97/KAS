    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        if (validar()) {
            try {
                String usuario = txtUsuario.getText();
                String password = String.copyValueOf(pswClave.getPassword());

                int tipoLogin = 0;
                if (rbtDirectivos.isSelected()) {
                    tipoLogin = 1;

                } else {
                    if (rbtDocentes.isSelected()) {
                        tipoLogin = 2;
                    } else {
                        if (rbtOtro.isSelected()) {
                            tipoLogin = 3;
                        }
                    }
                }
                if (g.existeUsuario(usuario, password, tipoLogin) == true) {
                    if (rbtDirectivos.isSelected()) {
                        principalDirectivo pd = new principalDirectivo();
                        pd.setVisible(true);
                    } else {
                        if (rbtDocentes.isSelected()) {
                            principalDocente pdc = new principalDocente();
                            pdc.setVisible(true);
                        } else {
                            if (rbtOtro.isSelected()) {
                                principalOtro po = new principalOtro();
                                po.setVisible(true);
                            } else {
                                JOptionPane.showMessageDialog(null, "Debe marcar un Perfil");
                            }
                        }
                    }
                this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Datos Incorrectos. Vuelva a ingresar.");
                    Limpiar();
                }

            } catch (Exception ex) {
                System.out.println("Metodo inicio de sesion MAL " + ex);
            }

        }


    }//GEN-LAST:event_btnAceptarActionPerformed

