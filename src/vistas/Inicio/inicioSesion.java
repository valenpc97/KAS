    private boolean validar() {
        if (txtUsuario.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar un Usuario");
            return false;
        }
        if (pswClave.getPassword().length > 1) {
        } else {
            JOptionPane.showMessageDialog(this, "Debe Ingresar una Contraseña");
            return false;
        }
        if (!rbtDirectivos.isSelected()) {
            if (!rbtDocentes.isSelected()) {
                if (!rbtOtro.isSelected()) {
                    JOptionPane.showMessageDialog(this, "Debe Seleccionar una opcion");
                    return false;
                }
            }
        }

        return true;
    }

    private void Limpiar() {

        txtUsuario.setText("");
        pswClave.setText("");
        btnGroup.clearSelection();
        rbtDirectivos.setSelected(false);
        rbtDocentes.setSelected(false);
        rbtOtro.setSelected(false);

    }
